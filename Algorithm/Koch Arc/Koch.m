p=[0 0;10 0];%初始两点位置
n=2;%起始点的个数
A=[cos(pi/3) -sin(pi/3);sin(pi/3) cos(pi/3)];%旋转变换矩阵A
for k=1:4
    d=diff(p)/3;%求解坐标之间的差值，从而计算需要的向量
    m=4*n-3;%计算下次迭代的点的个数
    q=p(1:n-1,:); p(5:4:m,:)=p(2:n,:);%向量赋值，点数据更新个插入
    p(2:4:m,:)=q+d;
    p(3:4:m,:)=q+d+d*A';
    p(4:4:m,:)=q+2*d;
    n=m;
end
plot(p(:,1),p(:,2))